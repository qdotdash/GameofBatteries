package com.qdotdash.gameofbatteries;

import android.content.Intent;
import android.support.v4.content.ContextCompat;
import android.support.v4.view.ViewPager;
import android.support.v7.app.AppCompatActivity;
import android.os.Bundle;
import android.view.View;
import android.view.ViewGroup;
import android.view.Window;
import android.view.WindowManager;
import android.widget.Button;
import android.widget.ImageView;
import android.widget.LinearLayout;

public class help extends AppCompatActivity implements View.OnClickListener {


    //public variable declarations
    /////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////view pager
    private ViewPager mPager;
    ////////////////////////////////////////////////////////////////////int layouts
    private int[] layouts = {R.layout.slide1,R.layout.slide2,R.layout.slide3,R.layout.slide4,R.layout.slide5,R.layout.slide6,R.layout.slide7,R.layout.slide8};
    ////////////////////////////////////////////////////////////////////mpageradapter
    private MpagerAdapter mpagerAdapter;
    ////////////////////////////////////////////////////////////////////layouts
    private LinearLayout Dots_layout;
    ////////////////////////////////////////////////////////////////////image view
    private ImageView[] dots;
    ////////////////////////////////////////////////////////////////////Button
    private Button BnNext;
    ////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        //Fullscreen make
        //////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////
        requestWindowFeature(Window.FEATURE_NO_TITLE);
        getWindow().setFlags(WindowManager.LayoutParams.FLAG_FULLSCREEN,WindowManager.LayoutParams.FLAG_FULLSCREEN);
        //////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////

        ///////////////////////////////////////////////////////////////skipping welcome page
        /////////////////////////////////////////////////////////////////////////////

        setContentView(R.layout.activity_help);

        //////////////////////////////////////////////////////////////////////////////Initialising views
        //////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////view pager
        mPager = findViewById(R.id.viewPager);
        mpagerAdapter = new MpagerAdapter(layouts,help.this);
        mPager.setAdapter(mpagerAdapter);
        //////////////////////////////////////////////////////////////////////////////Button
        BnNext = findViewById(R.id.bnnext);
        /////////////////////////////////////////////////////////////////////////////Layouts
        Dots_layout = findViewById(R.id.dotslayout);
        //////////////////////////////////////////////////////////////////////////////
        //////////////////////////////////////////////////////////////////////////////
        //////////////////////////////////////////////////////////////////////////////

        ////////////////////////////////////////////////////////////////creating dots
        createdots(0);
        //////////////////////////////////////////////////////////////////

        ////Listeners
        //////////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////Next Button listener
        BnNext.setOnClickListener(this);
        ///////////////////////////////////////////////////////////////////Page change listener
        mPager.addOnPageChangeListener(new ViewPager.OnPageChangeListener() {
            //////////////////////Page scrolling
            @Override
            public void onPageScrolled(int position, float positionOffset, int positionOffsetPixels) {

            }
            //////////////////////Page selection
            @Override
            public void onPageSelected(int position) {
                //////////////////////create dots at selected position
                createdots(position);

                //////////////////////////////////////starting and skipping
                if(position==layouts.length-1)
                {
                    BnNext.setText("HOME");
                    BnNext.setTextColor(getResources().getColor(R.color.gotitcolor));
                    BnNext.setBackgroundColor(getResources().getColor(R.color.orange));
                }
                else
                {
                    BnNext.setText("NEXT");
                    BnNext.setTextColor(getResources().getColor(R.color.textcolorsc));
                    BnNext.setBackgroundColor(getResources().getColor(R.color.welcomebar));
                }
                //////////////////////////////////////

            }
            ////////////////////////////////////////////////////////////////////////////
            @Override
            public void onPageScrollStateChanged(int state) {

            }
        });
        ////////////////////////////////////////////////////////////////////////////
    }

    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////Full dots creation
    private void createdots(int current_position){
        if(Dots_layout!=null)
            Dots_layout.removeAllViews();

        dots = new ImageView[layouts.length];

        for(int i =0;i<layouts.length;i++){
            dots[i] = new ImageView(this);
            if(i==current_position){
                dots[i].setImageDrawable(ContextCompat.getDrawable(this,R.drawable.largedots));
            }
            else{
                dots[i].setImageDrawable(ContextCompat.getDrawable(this,R.drawable.smalldotshelp));
            }
            LinearLayout.LayoutParams params = new LinearLayout.LayoutParams(ViewGroup.LayoutParams.WRAP_CONTENT,ViewGroup.LayoutParams.WRAP_CONTENT);
            params.setMargins(4,0,4,0);
            params.width=30;
            Dots_layout.addView(dots[i],params);
        }

    }
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////Next Onclick
    @Override
    public void onClick(View v) {
        switch(v.getId()){
            case R.id.bnnext:
                loadnextslide();
                break;

        }

    }
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////Load Home
    private void LoadHome()
    {
        startActivity(new Intent(this,MainActivity.class));
        finish();
    }
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////Load NExt slide
    private void loadnextslide()
    {
        int next_slide = mPager.getCurrentItem()+1;
        if(next_slide<layouts.length)
        {
            mPager.setCurrentItem(next_slide);
        }
        else{
            LoadHome();
        }
    }
    ////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////


}
